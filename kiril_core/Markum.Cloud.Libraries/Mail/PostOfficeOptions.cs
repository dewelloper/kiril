        /// <summary>
        ///     Specifies the Windows Security Realm to be used for Integrated Authentication
        /// </summary>
        private bool MappedDomain { get; set; }

        /// <summary>
        ///     Specifies whether Integrated Authentication for this postoffice supports UPN (User@securitydomain) formatted
