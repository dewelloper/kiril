namespace Markum.Cloud.Libraries.Mail
{
    public class PostOfficeOptions
    {
        /// <summary>
        ///     Determines whether the specified postoffice is mapped to a Security Windows Domain
        /// </summary>
        private const string MAPPEDDOMAINENABLED = "MappedDomainEnabled";

        /// <summary>
        ///     Specifies the Windows Security Realm to be used for Integrated Authentication
        /// </summary>
        private const string MAPPEDDOMAIN = "MappedDomain";

