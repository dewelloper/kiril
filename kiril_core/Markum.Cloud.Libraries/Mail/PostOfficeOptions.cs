        /// </summary>
        private bool UPNEnabled { get; set; }

        /// <summary>
        ///     Specifies whether the postoffice is mapped to a Windows Security Realm for Integrated Authentication
        /// </summary>
        private bool WindowsAuthenticationEnabled { get; set; }

        /// <summary>
        ///     Specifies whether accounts are to be automatically created if the user has authenticated using Integrated
        ///     Authentication
        /// </summary>
        private bool WindowsAccountAutoCreation { get; set; }

        /// <summary>
        ///     Specifies whether Web Administration is available for ADMIN or SYSADMIN users of this Post Office.
        /// </summary>
        public bool WebAdminEnabled { get; set; }

        /// <summary>
        ///     Determines whether ADMIN or SYSADMIN users are able to edit details of Mailboxes
        /// </summary>
        public bool WebAdminCanEditMailboxes { get; set; }

        /// <summary>
        ///     Determines the maximum number of Mailboxes that can be added via WebA dministration for this Post Office.
        /// </summary>
        public int WebAdminMaxMailboxes { get; set; }

