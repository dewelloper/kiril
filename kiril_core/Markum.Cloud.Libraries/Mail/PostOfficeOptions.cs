        /// </summary>
        private const string WINDOWSACCOUNTAUTOCREATION = "WindowsAccountAutoCreation";

        /// <summary>
        ///     Specifies whether Web Administration is available for ADMIN or SYSADMIN users of this Post Office.
        /// </summary>
        public const string WEBADMINENABLED = "WebAdmin-Enabled";

