        ///     Determines whether ADMIN or SYSADMIN users are able to edit the details for lists
        /// </summary>
        public bool WebAdminCanEditLists { get; set; }

        /// <summary>
        ///     Determines the maximum number of lists that can be managed by an ADMIN or SYSADMIN user
        /// </summary>
        public int WebAdminMaxLists { get; set; }

        /// <summary>
        ///     Determines the maximum number of list members that can be assigned to a list
        /// </summary>
        public int WebAdminMaxListMembers { get; set; }

        /// <summary>
        ///     Specifies whether ADMIN or SYSADMIN users are able to configure domain details via Web Administration
        /// </summary>
