        ///     Determines the maximum number of Mailboxes that can be added via WebA dministration for this Post Office.
        /// </summary>
        public const string WEBADMINMAXMAILBOXES = "WebAdmin-MaxMailboxes";

        /// <summary>
        ///     Determines the maximum Mailbox Quota Size that can be specified when creating or modifying a Mailbox
        /// </summary>
        public const string WEBADMINDEFAULTMAILBOXSIZE = "WebAdmin-DefaultMailboxSize";

        /// <summary>
        ///     Determines whether ADMIN or SYSADMIN users are able to specify Mailbox sizes
        /// </summary>
        public const string WEBADMINCANEDITMAILBOXSIZE = "WebAdmin-CanEditMailboxSize";

        /// <summary>
        ///     Determines whether ADMIN or SYSADMIN users are able to edit the details for lists
        /// </summary>
        public const string WEBADMINCANEDITLISTS = "WebAdmin-CanEditLists";

